			<h3>Lankomumo statistika</h3>
			<ul>
				<li>Į <strong>dirbtų posėdžių dalies</strong> sąrašą patenka po tris dažniausiai bei rečiausiai posėdžiuose dalyvaujančius Seimo narius pagal <a href="http://www3.lrs.lt/pls/inter/w5_smn_akt_new.seim_nar_lank_det?p_met=2011&amp;p_men=06&amp;p_var=1&amp;p_rus=0" title="Lietuvos Respublikos Seimas">oficialią Seimo statistiką.</a></li>

				<li>Į <strong>dirbtų valandų dalies</strong> sąrašą patenka po tris daugiausiai bei mažiausiai laiko Seime praleidusius asmenis. Dirbtų valandų dalis apskaičiuojama remiantis Seimo narių registracijų į balsavimus duomenimis (laikoma, kad Seimo narys dalyvavo posėdžio dalyje tarp dviejų registracijų, jei užsiregistravo bent vienoje jų).</li>

				<li>Į <strong>dirbtų valandų dalies ir dirbtų posėdžių dalies santykio</strong> sąrašą patenka po tris asmenis, turinčius aukščiausią bei žemiausią dirbtų valandų dalies bei dirbtų posėdžių dalies santykį. Pavyzdžiui, jei santykis yra 0.5, tai reiškia, jog Seimo narys Seime praleido dvigubai mažiau laiko nei teigia jo oficiali lankomumo statistika.</li>

				<li><strong>Dirbtų valandų dalis pagal frakcijas</strong> surikiuoja frakcijas pagal vidutinį jų narių lankomumą. Lankomumas apskaičiuojamas įvertinant kiekvieno Seimo nario dirbtų valandų dalį.</li>

				<li><strong>Dirbtų valandų dalis pagal mėnesius</strong> pateikia Seimo narių lankomumo statistiką pagal mėnesius. Lankomumas apskaičiuojamas įvertinant kiekvieno Seimo nario dirbtų valandų dalį.</li>

				<li><strong>Vidutinio Seimo nario darbo valandų statistika</strong> atspindi bendras Seimo darbo tendencijas. Kiekviena Seimo darbo diena apibūdinama vidutiniu vieno Seimo nario dirbtų bei nedirbtų valandų skaičiumi. Dirbtos valandos apskaičiuojamos remiantis Seimo narių registracijų į balsavimus duomenimis (laikoma, kad Seimo narys dalyvavo posėdžio dalyje tarp dviejų registracijų, jei užsiregistravo bent vienoje jų).</li>
			</ul>

			<h3>Balsavimo statistika</h3>
			<ul>
				<li><strong>Balsavo UŽ</strong> sąraše pateikiami trys dažniausiai bei trys rečiausiai <em>UŽ</em> balsuojantys Seimo nariai.</li>

				<li><strong>Balsavo PRIEŠ</strong> sąraše pateikiami trys dažniausiai bei trys rečiausiai <em>PRIEŠ</em> balsuojantys Seimo nariai.</li>

				<li><strong>Susilaikė</strong> sąraše pateikiami trys dažniausiai bei trys rečiausiai balsavimuose susilaikantys Seimo nariai.</li>

				<li><strong>Užsiregistravo, bet nebalsavo</strong> sąraše pateikiami trys daugiausiai bei trys mažiausiai kartų balsavimuose užsiregistravę, tačiau nebalsavę Seimo nariai. Jei Seimo narys balsavime užsiregistravo, bet nebalsavo, tai reiškia, jog jis balsavimo metu nepasirinko nei vienos iš trijų galimybių balsuojant (<em>UŽ</em>, <em>PRIEŠ</em>, <em>susilaikė</em>).</li>

				<li><strong>Balsavimų pasiskirstymas pagal dalyvių skaičių</strong> atvaizduoja balsavimų pasiskirstymą pagal juose dalyvavusių (užsiregistravusių) Seimo narių skaičių.</li>

				<li><strong>Bendra Seimo narių balsavimo statistika</strong> pateikia visų Seimo narių balsavimų pasirinkimų vidurkius.</li>

				<li><strong>Balsų pasiskirstymas pagal balsavimo rezultatą</strong> pateikia Seimo narių balsavimų pasirinkimų vidurkius, padalintus pagal balsavimo rezultatą. Balsavimo rezultatas gali būti <em>pritarta</em> arba <em>nepritarta</em> (<em>nepriimta</em>).</li>

				<li><strong>Balsų <em>UŽ</em> dalis balsavimuose, kurių rezultatas <em>NEPRITARTA</em></strong> atvaizduoja tokių balsavimų pasiskirstymą pagal jiems pritarusių balsų dalį (skaičiuojant tik užsiregistravusių Seimo narių pasirinkimus).</li>
			</ul>

			<h3>Individuali statistika</h3>
			<ul>
				<li><strong>Seimo nario balsavimo statistika</strong> pateikia Seimo nario balsavimo duomenis bei leidžia juos palyginti su bendru Seimo vidurkiu.</li>

				<li><strong>Seimo nario darbo valandų statistika</strong> atspindi Seimo nario darbo tendencijas. Kiekviena Seimo nario darbo diena apibūdinama asmens dirbtų bei nedirbtų valandų skaičiumi. Šie dydžiai apskaičiuojami remiantis Seimo narių registracijų į balsavimus duomenimis (laikoma, kad Seimo narys dalyvavo posėdžio dalyje tarp dviejų registracijų, jei užsiregistravo bent vienoje jų).</li>
			</ul>
			
			<h3>Posėdžių statistika</h3>
				<ul>
					<li><strong>Bendras dalyvių skaičius posėdžio metu</strong> pateikia Seimo narių skaičių, dalyvavusių posėdyje tam tikru laiku (laikoma, kad Seimo narys dalyvavo posėdžio dalyje tarp dviejų registracijų, jei užsiregistravo bent vienoje jų).</li>
	
					<li><strong>Individualių Seimo narių dalyvavimo posėdyje</strong> statistika pateikia kievieno Seimo nario darbo posėdyje statistiką tam tikru laiku (laikoma, kad Seimo narys dalyvavo posėdžio dalyje tarp dviejų registracijų, jei užsiregistravo bent vienoje jų).</li>
				</ul>
